#Meu Setimo Programa
#Trabalhando com laço de repetição FOR

lista1  = [1,2,3,4,5]

lista2 = ["ola","mundo","!"]

lista3 = [0,"ola","biscoito","bolacha",9.99,True]

for i in lista1: #A variavel I percorre toa a lista1
    print(i) #Mostra no console os valores da lista1

for z in lista2:#A variavel Z percorre toa a lista2
    print(z) #Mostra no console os valores da lista2

for x in lista3:#A variavel X percorre toa a lista3
    print(x) #Mostra no console os valores da lista3
