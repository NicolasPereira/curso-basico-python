'''
Escreva um programa que compare se duas sequências digitadas pelo usuário são iguais.
'''

txt1 = str(input("Digite a primeira sequencia: "))
txt2 = str(input("Digite a primeira sequencia: "))

if txt1 == txt2:
    print("As sequencias são iguais")
else:
    print("As sequencias não são iguais")
